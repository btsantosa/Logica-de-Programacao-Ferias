# Questão 02 : Sistema de Avaliação de Risco de Segurança em um Aeroporto Você está programando um sistema de segurança para um aeroporto. O código a seguir avalia o risco de segurança de passageiros com base em comportamentos suspeitos. O sistema registra comportamentos de risco e calcula uma pontuação de risco para cada passageiro, com base nos tipos de comportamento identificado. Dependendo da pontuação total, o passageiro será direcionado para uma inspeção mais rigorosa.

# O código deve permitir que o número de passageiros seja definido pelo usuário.
# Cada passageiro pode apresentar diferentes comportamentos suspeitos, como:

# Comportamento nervoso (pontuação de 5)
# Maleta não registrada (pontuação de 7)
# Vaga de estacionamento errada (pontuação de 3)
# Acompanhante suspeito (pontuação de 6)
# Outros comportamentos (pontuação de 4)

# O sistema deve calcular a pontuação de risco de cada passageiro com base nos comportamentos 
# identificados e determinar se o passageiro deve passar por uma inspeção adicional:

# Se a pontuação de risco for maior que 15, o passageiro será encaminhado para uma inspeção rigorosa.
# Caso contrário, o passageiro será liberado para embarque.
# Ao final perguntar se deseja avaliar outro passageiro